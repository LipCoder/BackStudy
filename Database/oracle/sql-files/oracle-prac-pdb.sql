CREATE TABLE MEMBER
(
    ID          VARCHAR2(50),
    PWD         NVARCHAR2(50),
    NAME        NVARCHAR2(50),
    GENDER      NCHAR(2),      -- 남성, 여성  UTF
    AGE         NUMBER(3),
    BIRTHDAY    CHAR(10),     -- 2000-01-02
    PHONE       CHAR(13),     -- 010-1234-2345
